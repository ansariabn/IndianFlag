package All;

public class swap_no {
    public static void main(String[] args) {

        int Num1 = 35;
        int Num2 = 25;
        System.out.println("Num1 :"+Num1);
        System.out.println("Num2 :"+Num2);

        int Num3 = Num2;
        Num2 = Num1;
        Num1 = Num3;
        System.out.println("Num1 :"+Num1);
        System.out.println("Num2 :"+Num2);


    }
//
}
